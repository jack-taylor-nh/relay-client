  // Handle streaming AI request
  const handleStreamingRequest = async (
    requestId: string,
    decryptedPayload: any,
    clientPublicKey: string,
    operatorEdgeId: string,
    startTime: number
  ) => {
    let fullContent = '';
    let promptTokens = 0;
    let completionTokens = 0;
    let streamingFailed = false; // Flag to detect 404 and stop processing
    let chunkSequence = 0; // Track chunk sequence for Redis Streams
    let firstTokenTime: number | null = null; // Track TTFT (Time To First Token)

    try {
      // STEP 2: Call local Ollama with streaming enabled
      const response = await fetch('http://localhost:11434/v1/chat/completions', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          model: decryptedPayload.model,
          messages: decryptedPayload.messages,
          temperature: decryptedPayload.temperature ?? 0.7,
          max_tokens: decryptedPayload.max_tokens ?? 2048,
          stream: true, // Enable streaming
        }),
      });

      if (!response.ok) {
        throw new Error(`Ollama returned ${response.status}`);
      }

      // STEP 3: Parse SSE stream from Ollama
      const reader = response.body!.getReader();
      const decoder = new TextDecoder();
      let buffer = '';

      while (true) {
        const { done, value } = await reader.read();
        if (done) break;

        buffer += decoder.decode(value, { stream: true });
        const lines = buffer.split('\n');
        buffer = lines.pop() || '';

        for (const line of lines) {
          if (!line.trim() || !line.startsWith('data: ')) continue;
          
          const jsonStr = line.replace('data: ', '').trim();
          if (jsonStr === '[DONE]') continue;

          // Skip processing if streaming endpoint failed
          if (streamingFailed) continue;

          try {
            const chunk = JSON.parse(jsonStr);
            
            // Extract token content
            const delta = chunk.choices?.[0]?.delta;
            if (delta?.content) {
              const token = delta.content;
              fullContent += token;
              
              // Capture TTFT (Time To First Token) on first token arrival
              if (!firstTokenTime) {
                firstTokenTime = Date.now() - startTime;
              }
              
              // STEP 4: Encrypt token chunk
              const encryptedChunk = await encryptE2EEPayload(
                JSON.stringify({ content: token }),
                clientPublicKey
              );
              
              // STEP 5: Send encrypted chunk to router with sequence number
              const chunkResponse = await fetch(`${RELAY_AI_ROUTER_URL}/v1/ai/stream-chunk`, {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify({
                  request_id: requestId,
                  encrypted_chunk: {
                    ciphertext: encryptedChunk.ciphertext,
                    ephemeral_pubkey: encryptedChunk.ephemeralPublicKey,
                    nonce: encryptedChunk.nonce,
                  },
                  sequence: chunkSequence++, // Add sequence number
                  done: false,
                }),
              });
              
              // If endpoint doesn't exist (404), set flag and throw error to trigger fallback
              if (chunkResponse.status === 404) {
                streamingFailed = true;
                throw new Error('Streaming endpoint not available (404)');
              }
            }
            
            // Track usage from final chunk
            if (chunk.usage) {
              promptTokens = chunk.usage.prompt_tokens || promptTokens;
              completionTokens = chunk.usage.completion_tokens || completionTokens;
            }
          } catch (parseErr) {
            // If streaming failed due to 404, re-throw to trigger fallback
            if (streamingFailed) {
              throw parseErr;
            }
            console.warn('[Streaming] Failed to parse chunk:', parseErr);
          }
        }
      }

      // STEP 6: Send final metadata chunk (only if streaming didn't fail)
      if (streamingFailed) {
        throw new Error('Streaming endpoint not available (404)');
      }
      
      const latency = Date.now() - startTime;
      const totalTokens = promptTokens + completionTokens || Math.ceil((JSON.stringify(decryptedPayload.messages).length + fullContent.length) / 4);
      
      const metadata = {
        tokens_used: totalTokens,
        prompt_tokens: promptTokens || Math.ceil(JSON.stringify(decryptedPayload.messages).length / 4),
        completion_tokens: completionTokens || Math.ceil(fullContent.length / 4),
        model: decryptedPayload.model,
        finish_reason: 'stop',
        operator_edge_id: operatorEdgeId,
        processing_time_ms: firstTokenTime || latency, // Use TTFT latency for streaming
      };
      
      // Encrypt metadata before sending
      const encryptedMetadata = await encryptE2EEPayload(
        JSON.stringify(metadata),
        clientPublicKey
      );
      
      const finalResponse = await fetch(`${RELAY_AI_ROUTER_URL}/v1/ai/stream-chunk`, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          request_id: requestId,
          encrypted_chunk: {
            ciphertext: encryptedMetadata.ciphertext,
            ephemeral_pubkey: encryptedMetadata.ephemeralPublicKey,
            nonce: encryptedMetadata.nonce,
          },
          sequence: chunkSequence++, // Add sequence number to final chunk
          done: true,
          metadata,
        }),
      });
      
      // Check for 404 on final chunk as well
      if (finalResponse.status === 404) {
        throw new Error('Streaming endpoint not available (404)');
      }

      // Update stats
      const earnings = (totalTokens / 1000) * (selectedModel?.payoutPer1kTokens || 0.15);
      setStats(prev => ({
        totalRequests: prev.totalRequests + 1,
        successfulRequests: prev.successfulRequests + 1,
        failedRequests: prev.failedRequests,
        totalTokens: prev.totalTokens + totalTokens,
        avgLatencyMs: ((prev.avgLatencyMs * prev.totalRequests) + latency) / (prev.totalRequests + 1),
        estimatedEarnings: prev.estimatedEarnings + earnings,
      }));

      // Add log
      setRequestLogs(prev => [{
        id: requestId,
        timestamp: Date.now(),
        model: decryptedPayload.model,
        tokensIn: metadata.prompt_tokens,
        tokensOut: metadata.completion_tokens,
        latencyMs: latency,
        status: 'success',
        earnings,
        isE2EE: true,
      }, ...prev].slice(0, 50));

      console.log(`[E2EE Operator] Successfully streamed request ${requestId} in ${latency}ms`);

    } catch (err: any) {
      console.error('[Streaming] Error:', err);
      throw err;
    }
  };

